/**
 * op_add - return the sum of 2 numbers
 * @a: first integer
 * @b: second integer
  * Return: sun of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns difference of 2 numbers
 * @a: first integer
 * @b: second integer
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns product of 2 numbers
 * @a: first interger
 * @b: second integer
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the division of 2 numbers
 * @a: first integer
 * @b: second integer
 * Return: the division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -returns the remainder of the division of 2 integers
 * @a: first integer
 * @b: second integer
 * Return: remainder of a divided b
 */

int op_mod(int a, int b)
{
	return (a % b);
}






























